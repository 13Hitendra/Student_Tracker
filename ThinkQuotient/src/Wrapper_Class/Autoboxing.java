package Wrapper_Class;

public class Autoboxing {
	public static void main(String[] args) {
		byte b = 10;
		Byte B = b;

		short s = 15;
		Short S = s;

		int i = 20;
		System.out.println(i);
		Integer I = i;
		System.out.println(I);

		long l = 25;
		Long L = l;

		float f = 12;
		Float F = f;

		double d = 18.58;
		Double D = d;

		boolean bln = true;
		Boolean BLN = bln;

		char c = 'C';
		Character C = c;

	}
}
